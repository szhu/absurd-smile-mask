  // graphic commands to redraw the complete screen should be placed here
  switch (lastCommand) {
  case 0:
    u8g.drawBitmapP(0, 0, 6, 50, brainy_bitmap);
    break;

  case 30:
    u8g.drawBitmapP(0, 0, 6, 50, joy_bitmap);
    break;

  case 34:
    u8g.drawBitmapP(0, 0, 6, 50, mouth_smile);
    break;

  case 36:
    u8g.drawBitmapP(0, 0, 6, 50, mouth_default);
    break;

  case 21:
    u8g.drawBitmapP(0, 0, 6, 50, mouth_wow);
    break;

  case 27:
    u8g.drawBitmapP(0, 0, 16, 64, mouth_new);
    break;
  }

